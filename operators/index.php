  <h1>Operadores</h1>

  <p>Ordem de precedencia:</p>
  <ol>
    <li>( )</li> Parenteses
    <li>**</li> Potenciação
    <li>* e / e %</li> Multiplicação, Divisão e Modulo
    <li>+ e -</li> Soma e Subtração
  </ol>

  <p>Funções:</p>
  <ul>
    <li>abs()</li>Para tornar o valor absoluto
    <li>base_convert()</li>Conversão para base octa, dexacimal ou unicode
    <li>ceil(), floor(), round()</li>Arredondamento para cima, baixo e aritimético
    <li>hypot()</li>Calcular hipotenusa
    <li>intdiv()</li>Divisão inteira de dois números
    <li>min() e max()</li>Tirar o menor ou o maior dentre os valores
    <li>pi()</li>Valor de pi ou usar a const pi = M_PI
    <li>pow()</li>Potenciação - <i>Diferência de usar a função e os operadores é a precedência</i> 
    <li>sin(), cos(), tan()</li>Seno Coseno e Tangente
    <li>sqrt()</li>Raiz quadrada - <i>Pode ser feita na operação inversa, poteência(Exemplo: 81**(1/2)=9)</i>
  </ul>
